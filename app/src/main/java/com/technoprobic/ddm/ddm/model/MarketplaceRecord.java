package com.technoprobic.ddm.ddm.model;

public class MarketplaceRecord {

    // current Decentralized Marketplace Record

    String marketplaceSeller;
    String marketplaceSensorDescription;
    String marketplaceSessionDescription;
    String marketplaceLatitude;
    String marketplaceLongitude;
    String marketplacePrice;
    String marketplaceIpAddress;
    String marketplacePublicIpAddress;

    public MarketplaceRecord() {

    }

    public String getMarketplaceSeller() {
        return marketplaceSeller;
    }

    public void setMarketplaceSeller(String marketplaceSeller) {
        this.marketplaceSeller = marketplaceSeller;
    }

    public String getMarketplaceSensorDescription() {
        return marketplaceSensorDescription;
    }

    public void setMarketplaceSensorDescription(String marketplaceSensorDescription) {
        this.marketplaceSensorDescription = marketplaceSensorDescription;
    }

    public String getMarketplaceSessionDescription() {
        return marketplaceSessionDescription;
    }

    public void setMarketplaceSessionDescription(String marketplaceSessionDescription) {
        this.marketplaceSessionDescription = marketplaceSessionDescription;
    }

    public String getMarketplaceLatitude() {
        return marketplaceLatitude;
    }

    public void setMarketplaceLatitude(String marketplaceLatitude) {
        this.marketplaceLatitude = marketplaceLatitude;
    }

    public String getMarketplaceLongitude() {
        return marketplaceLongitude;
    }

    public void setMarketplaceLongitude(String marketplaceLongitude) {
        this.marketplaceLongitude = marketplaceLongitude;
    }

    public String getMarketplacePrice() {
        return marketplacePrice;
    }

    public void setMarketplacePrice(String marketplacePrice) {
        this.marketplacePrice = marketplacePrice;
    }

    public String getMarketplaceIpAddress() {
        return marketplaceIpAddress;
    }

    public void setMarketplaceIpAddress(String marketplaceIpAddress) {
        this.marketplaceIpAddress = marketplaceIpAddress;
    }

    public String getMarketplacePublicIpAddress() {
        return marketplacePublicIpAddress;
    }

    public void setMarketplacePublicIpAddress(String marketplacePublicIpAddress) {
        this.marketplacePublicIpAddress = marketplacePublicIpAddress;
    }
}
